public class WeightCalculator
{
	public static void main (String[] args)
	{
		double wght = 200;
		double Mars;
		double Jupiter;
		double Venus;
		double Saturn;
		
		Mars = wght * 0.38;
		Jupiter = wght * 2.36;
		Venus = wght * 0.91;
		Saturn = wght * 1.06;
		System.out.println(Mars);
		System.out.println(Jupiter);
		System.out.println(Venus);
		System.out.println(Saturn);
	}
}